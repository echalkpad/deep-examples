package com.stratio.deep.examples.utils;

import com.stratio.deep.commons.annotations.DeepEntity;
import com.stratio.deep.commons.annotations.DeepField;
import com.stratio.deep.commons.entity.IDeepType;

/**
 * Class used in examples.
 */
@DeepEntity
public class MessageTestEntity implements IDeepType {

    @DeepField(fieldName = "id", isPartOfClusterKey = true, isPartOfPartitionKey = true)
    private String id;

    @DeepField
    private String message;

    @DeepField
    private Long number;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Long getNumber() {
        return number;
    }

    public void setNumber(Long number) {
        this.number = number;
    }
}
